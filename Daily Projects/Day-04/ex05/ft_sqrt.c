int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 0;
	res = 0;
	if (nb == 1)
		return (1);
	else if (nb <= 0)
		return (0);
	else
	{
		i = 1;
		while (i <= nb / 2)
		{
			if (nb == i * i)
			{
				res = i;
				break ;
			}
			i++;
		}
	}
	return (res);
}
