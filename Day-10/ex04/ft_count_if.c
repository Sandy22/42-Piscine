int		ft_count_if(char **tab, int (*f)(char*))
{
	int count;
	int res;

	count = 0;
	res = 0;
	while (*tab != 0)
	{
		res = f(*tab);
		tab++;
		if (res == 1)
		{
			count++;
			res = 0;
		}
	}
	return (count);
}
