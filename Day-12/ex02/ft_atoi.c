int		ft_atoi(char *str)
{
	int flag;
	int i;
	int num;

	i = 0;
	flag = 1;
	num = 0;
	while ((*(str + i) >= 9 && *(str + i) <= 13) || *(str + i) == 32)
		i++;
	if (*(str + i) == '-' || *(str + i) == '+')
	{
		if (*(str + i) == '-')
			flag = -1;
		i++;
	}
	while ((*(str + i) != '\0') && (*(str + i) >= '0') && (*(str + i) <= '9'))
	{
		num = num * 10 + (*(str + i) - 48);
		i++;
	}
	num = num * flag;
	return (num);
}
